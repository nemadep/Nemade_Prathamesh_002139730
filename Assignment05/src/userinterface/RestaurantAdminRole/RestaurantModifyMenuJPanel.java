    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Boolean valid = validateMenuEntry();
        if (valid) {
            this.resMenu.put(this.menuNameJField.getText(), Double.parseDouble(this.menuPriceJField.getText()));
            _getMenu();
            _setMenuList();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (this.selectedIndex != -1) {
            String key = null;
            Integer count = 0;
            for (HashMap.Entry<String, Double> entry : this.resMenu.entrySet()) {
                if (count == this.selectedIndex) {
                    key = entry.getKey();
                }
                count += 1;
            }
            this.resMenu.remove(key);
            _getMenu();
            _setMenuList();

        } else {
            JOptionPane.showMessageDialog(this, "Please select a menu to Delete!", "Delete Menu", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        for (int i = 0; i < this.system.getRestaurantDirectory().getRestaurantList().size(); i++) {
            Restaurant ongoing = this.system.getRestaurantDirectory().getRestaurantList().get(i);
            if (ongoing == this.selectedRes) {
                ongoing.setMenu(resMenu);
            }
        }
        _getMenu();
        _setMenuList();
        JOptionPane.showMessageDialog(this, "Menu modififed successfully!!", "Modidy Menu", INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton4ActionPerformed


