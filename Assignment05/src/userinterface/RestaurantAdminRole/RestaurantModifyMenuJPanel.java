    public boolean validateMenuEntry() {
        RestaurantDirectory dummy = this.system.getRestaurantDirectory();
        String errorMessage = "";
        String errorName = dummy.validateName(this.menuNameJField.getText());
        String errorPrice = dummy.validatePrice(this.menuPriceJField.getText());
        errorMessage += errorName;
        errorMessage += errorPrice;

        if (errorMessage.equals("")) {
            return true;
        }
        JOptionPane.showMessageDialog(this, errorMessage, "Create Restaurant", ERROR_MESSAGE);
        return false;
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.selectedIndex != -1) {
            Integer count = 0;
            for (HashMap.Entry<String, Double> entry : this.resMenu.entrySet()) {
                String key = entry.getKey();
                Double value = entry.getValue();
                if (count == this.selectedIndex) {
                    this.menuNameJField.setText(key);
                    this.menuPriceJField.setText(String.valueOf(value));
                }
                count += 1;
            }
            _getMenu();
            _setMenuList();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a menu to Update!", "Update Menu", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

