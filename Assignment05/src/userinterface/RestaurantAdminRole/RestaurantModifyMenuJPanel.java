    public void _getMenu() {
        for (int i = 0; i < this.system.getRestaurantDirectory().getRestaurantList().size(); i++) {
            Restaurant ongoing = this.system.getRestaurantDirectory().getRestaurantList().get(i);
            System.out.println("ongoing.getManagerUserName()" + ongoing.getManagerUserName() + "|" + this.account.getUsername());
            if (ongoing.getManagerUserName().toString().equals(this.account.getUsername().toString())) {
                resMenu = ongoing.getMenu();
                selectedRes = ongoing;
            }
        }
    }

    public void _setMenuList() {
        DefaultListModel model = new DefaultListModel();
        jList1.setModel(model);
        for (Iterator<Map.Entry<String, Double>> it = this.resMenu.entrySet().iterator(); it.hasNext();) {
            HashMap.Entry<String, Double> set = it.next();
            model.addElement(set.getKey() + " - " + set.getValue());
        }
        jList1.setModel(model);
    }

