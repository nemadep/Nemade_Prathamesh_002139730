    /**
     * dynamic validator used to validate the input field based on the label
     *
     * @param validate
     * @param label
     * @return
     */
    private boolean genericValidateHandler(String validate, JLabel label) {
        Boolean isValid = false;
        if (!validate.equals("")) {
            label.setText(validate);
        } else {
            label.setText("");
            isValid = true;
        }
        return isValid;
    }

