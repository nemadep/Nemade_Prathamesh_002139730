    private void dateOfBirthFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_dateOfBirthFocusLost
        String value = dobDayJField.getText().trim() + "/" + dobMonthJField.getText().trim() + "/" + dobYearJField.getText().trim();
        Boolean isValid = genericValidateHandler(person.validateDOB(value), dobErrorJLabel);
        DateFormat dateFormatter = new SimpleDateFormat("dd/MM/yyyy");
        if (isValid) {
            try {
                dateFormatter.setLenient(false);
                person.setDateOfBirth(dateFormatter.parse(value));
            } catch (ParseException ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_dateOfBirthFocusLost

